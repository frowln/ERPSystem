package com.privod.platform.modules.hrRussian.domain;

/**
 * Типы дней производственного календаря РФ.
 */
public enum DayType {
    /** Рабочий день (8.0 ч) */
    WORKING,
    /** Выходной (суббота/воскресенье, 0 ч) */
    WEEKEND,
    /** Праздничный нерабочий день (0 ч) */
    HOLIDAY,
    /** Предпраздничный день (7.0 ч — сокращённый на 1 час) */
    PRE_HOLIDAY,
    /** Перенесённый выходной (0 ч — бывший рабочий день стал выходным) */
    TRANSFERRED_WEEKEND,
    /** Перенесённый рабочий день (8.0 ч — бывший выходной стал рабочим) */
    TRANSFERRED_WORKING
}
